"""
String em python pode ser referida como str
String são textos dentro de aspas
Tudo que estiver dentro de aspas é considerado como string dentro de python
"""

"""
Python é uma linguagem de tipagem dinâmica
"""

print('Essa é uma "string".')

# Caracter de escape para utilizar o mesmo tipo de aspas dentro de uma string
print('Esse é o meu \'texto\' (str).')
# Ao declarar a letra r antes da string você diz para o interpretador do python não executar nada além disso,
# é apenas para ser lido como uma string
print(r'Esse é o meu \n (str).')  # No caso o \n não será executado como quebra de linha
